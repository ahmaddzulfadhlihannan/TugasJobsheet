public class ArrayBilangan03 {
    public static void main(String[] args) {
        
        //Variabel
        int[] bil = new int [4];

        //Nilai indeks
        bil[0] = 5;
        bil[2] = 13;
        bil[2] = -7;
        bil[3] = -17;

        //Tampilkan nilai indeks
        System.out.println(bil[0]);
        System.out.println(bil[1]);
        System.out.println(bil[2]);
        System.out.println(bil[3]);

    }
}